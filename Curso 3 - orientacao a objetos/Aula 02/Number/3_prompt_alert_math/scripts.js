// A função console.log vai ser utilizada junto aos outros métodos, porque serve como
//um print e debugger para verificarmos o que foi feito de função até o momento.

// para utilizar a função prompt:
let idade = prompt("Qual a sua idade?");

console.log(idade);

let nome = prompt("Qual o seu nome?");

console.log(`O meu nome é ${nome} e minha idade é ${idade} anos.`);

// para utilizar a função alert:
alert("Esta é a mensagem!");
alert("Esta é a mensagem 2!");

// para utilizar a função math:
let maiorNumero = Math.max(1,5,2,3);
console.log(maiorNumero)

let menorNumero = Math.min(1,5,2,3);
console.log(menorNumero)

let arredondar = Math.round(5.2934723);
console.log(arredondar)

let arredondarParaCima = Math.ceil(5.2934723);
console.log(arredondarParaCima)

